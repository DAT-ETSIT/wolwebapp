## Copia este ejemplo con el nombre 'mailConfig.py' y rellena los datos de inicio de sesión para tu servidor de correo.
mailName = "Wake on LAN Server" # Nombre visible en el campo de remitente.
mailUser = "no-reply@example.com" # Dirección de correo desde la que se enviarán los mensajes.
mailPass = "pass" # Contraseña de la cuenta de correo.
mailServer = "smtp.example.com" # Dirección del servidor SMTP.
mailPort = 587 # Puerto de SMTP. 465 para SSL, 587 para STARTTLS.